# operasi aritmatika

a = 10
b = 3

# sums with +
hasil = a + b
print(a, "+", b, "=", hasil)

# pengurangan with - 
hasil = a - b
print(a, "-", b, "=", hasil)

# multiply with *
hasil = a * b
print(a, "*", b, "=", hasil)

# divide with /
hasil = a / b
print(a, ":", b, "=", hasil)

# exponential
hasil = a ** b
print(a, "pangkat", b, "=", hasil)

# sisa pembagiaan
hasil = a % b
print(a, "%", b, "=", hasil)

# floor division // (dibulatkan kbwh)
hasil = a // b
print(a, "//", b, "=", hasil)

z = 8
y = 3
x = 6

hasil = x ** y * z / y + y - z // x % y
print(x,"**",y,"*",z,"/",y,"+",y,"-",z,'//',x,"%",y, "=", hasil)